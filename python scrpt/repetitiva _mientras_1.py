'''
repetitiva_mientras_1.py
script en Python que muestre los numeros del 0 al 13 utulizando
la estructura repetitiva "Mientras
'''
print('Programa que muestra los numeros del 0 al 13')
#contador
numero = 0

while numero <= 13:
    print(f'contador: {numero}')
    numero = numero + 1
else:
    print('He terminado de contar')

print('Nos vemos')